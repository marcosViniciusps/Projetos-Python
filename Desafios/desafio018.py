# Este programa le um angulo qualquer em graus e exibe seu seno, cosseno e tangente
import math
angulo = float(input('Digite o valor do angulo: '))
print('O seno de {} em radianos é: {}'.format(angulo,math.sin(angulo)))
print('O cosseno de {} em radianos é: {}'.format(angulo,math.cos(angulo)))
print('A tangente de {} em radianosé: {}'.format(angulo,math.tan(angulo)))

